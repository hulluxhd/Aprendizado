// MATH

// Math é um objeto que tem seus próprios métodos, mas não é um construtor, portanto, não pode ser instanciado
// em vez disso, pode-se usar seus métodos para realizar operações matemáticas, como exponenciação, raiz quadrada, menor número
// maior número, arredondamento (para cima ou para baixo), etc.


// arredonda os números
console.log(Math.round(2.34))
// arredonda para cima
console.log(Math.ceil(2.9))
// arredonda para baixo
console.log(Math.floor(2.9))
// raiz quadrada
console.log(Math.sqrt(9))
// mostra uma constante
console.log(Math.LN10)
console.log(Math.PI)



